  getGroups(): LevelGroup[] {
    return this.groups;
  }

  getLevel(level: number, groupName: string): string[][] | undefined {
    for (const group of this.groups) {
      if (group.group === groupName) {
        return group.levels[level];
      }
    }
    return undefined;
  }

  getGroupForLevel(level: number): string | undefined {
    for (const group of this.groups) {
      if (group.levels[level]) {
        return group.group;
      }
    }
    return undefined;
  }

  getMaxLevel(): number {
    let max = 0;
    for (const group of this.groups) {
      const groupMax = Math.max(...Object.keys(group.levels).map(Number));
      if (groupMax > max) {
        max = groupMax;
      }
    }
    return max;
  }

  getLevelsInGroup(groupName: string): number[] {
    for (const group of this.groups) {
      if (group.group === groupName) {
        return Object.keys(group.levels).map(Number).sort((a, b) => a - b);
      }
    }
    return [];
  }

  getGroupLevelsCount(groupName: string): number {
    return this.getLevelsInGroup(groupName).length;
  }
